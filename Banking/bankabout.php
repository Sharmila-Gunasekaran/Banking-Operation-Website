        <p>The Housing Development Finance Corporation Limited or HDFC Ltd was among the first financial institutions in India to receive an “in principle” approval from the Reserve Bank of India (RBI) to set up a bank in the private sector. This was done as part of RBI’s policy for liberalisation of the Indian banking industry in 1994.</p>

           <p> HDFC Bank was incorporated in August 1994 in the name of HDFC Bank Limited, with its registered office in Mumbai, India. The bank commenced operations as a Scheduled Commercial Bank in January 1995.</p>
            
            
